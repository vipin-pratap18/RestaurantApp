package com.restaurant.model;

public class RestaurantItems {

	private String restItemId;
	private String restID;
	private String itemID;
	private String restItemPopularity;
	private String showInCatalogue;
	private String itemName;
	private String restName;
	
	public String getRestItemId() {
		return restItemId;
	}
	public void setRestItemId(String restItemId) {
		this.restItemId = restItemId;
	}
	public String getRestID() {
		return restID;
	}
	public void setRestID(String restID) {
		this.restID = restID;
	}
	public String getItemID() {
		return itemID;
	}
	public void setItemID(String itemID) {
		this.itemID = itemID;
	}
	public String getRestItemPopularity() {
		return restItemPopularity;
	}
	public void setRestItemPopularity(String restItemPopularity) {
		this.restItemPopularity = restItemPopularity;
	}
	public String getShowInCatalogue() {
		return showInCatalogue;
	}
	public void setShowInCatalogue(String showInCatalogue) {
		this.showInCatalogue = showInCatalogue;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getRestName() {
		return restName;
	}
	public void setRestName(String restName) {
		this.restName = restName;
	}
	
	
}
